#include <stdio.h>

int main()
{
    int broj = 7;
    printf("broj = %i\n", broj);
    printf("broj++ = %i\n", broj++);
    printf("broj posle  uvecanja %i\n", broj);
    printf("++broj = %i\n", ++broj);
    printf("broj posle  uvecanja %i\n", broj);
    printf("broj-- = %i\n", broj--);
    printf("broj posle  umanjenja %i\n", broj);
    printf("--broj = %i\n", --broj);
    printf("broj posle  umanjenja %i\n", broj);
    return 0;
}
